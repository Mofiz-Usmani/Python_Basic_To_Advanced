# ==================
# For Loop in Python
# ==================




# Example 1: Loop over a list
fruits = ["apple", "banana", "cherry"]

for fruit in fruits:
    print(fruit)




# Example 2: Loop over a string
name = "Jackal"

for char in name: 
    print(char)






# Example 3: Loop with range()
# numbers from 0 to 4
for i in range(5):
    print(i)



# numbers from 1 to 5
for i in range(1, 6):
    print(i)






# Example 4: Sum of numbers using for loop
total = 0
for i in range(1, 6):
    total += i

print("Sum =", total)

